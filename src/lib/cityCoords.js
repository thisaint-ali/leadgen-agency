/**
 * Parse a location string like "Houston, TX" or "Fairfax, VA" → [lng, lat]
 * Falls back to state center if city not in lookup.
 */
export function parseLocation(location) {
  if (!location) return null;
  const trimmed = location.trim();

  // Direct match
  if (CITY_COORDS[trimmed]) return CITY_COORDS[trimmed];

  // Try "City, ST" where ST might have extra spaces
  const parts = trimmed.split(',');
  if (parts.length >= 2) {
    const city = parts[0].trim();
    const state = parts[1].trim().toUpperCase().slice(0, 2);
    const key = `${city}, ${state}`;
    if (CITY_COORDS[key]) return CITY_COORDS[key];
    // Fall back to state center
    if (STATE_CENTERS[state]) return STATE_CENTERS[state];
  }

  return null;
}
